import Foundation
import Testing

@Suite struct PluginSmokeTests {
    #if compiler(>=6.4) && !(os(iOS) || os(watchOS) || os(tvOS) || os(visionOS))
        @Test(
            .disabled(
                if: ProcessInfo.processInfo.environment["__XCODE_BUILT_PRODUCTS_DIR_PATHS"] != nil,
                "\"swift package resolve\" fails to clone the git repository only under the Xcode test runner"
            )
        )
        func extractPlugin() throws {
            let result = try assertSwiftPackage(
                fixturePackage: "PluginSmokePackage", ["--target", "PluginSmokeModule"])
            #expect(
                result.witContents == """
                    // DO NOT EDIT.
                    //
                    // Generated by the WITExtractor
                    package swift:plugin-smoke-package;

                    interface plugin-smoke-module {
                        record struct-A {
                            member-B: s64,
                        }

                        record struct-A-nested-struct-C {
                            member-D: s64,
                        }
                    }
                    """)
        }

        @Test(
            .disabled(
                if: ProcessInfo.processInfo.environment["__XCODE_BUILT_PRODUCTS_DIR_PATHS"] != nil,
                "\"swift package resolve\" fails to clone the git repository only under the Xcode test runner"
            )
        )
        func fixtureCompilesWithMarker() throws {
            try assertSwiftBuilds(fixturePackage: "PluginSmokePackage")
        }

        @Test(
            .disabled(
                if: ProcessInfo.processInfo.environment["__XCODE_BUILT_PRODUCTS_DIR_PATHS"] != nil,
                "\"swift package resolve\" fails to clone the git repository only under the Xcode test runner"
            )
        )
        func inlinesCrossModuleType() throws {
            let result = try assertSwiftPackage(
                fixturePackage: "CrossModulePackage", ["--target", "CrossModuleAPI"])
            #expect(
                result.witContents == """
                    // DO NOT EDIT.
                    //
                    // Generated by the WITExtractor
                    package swift:cross-module-package;

                    interface cross-module-API {
                        record uses-external {
                            size: s64,
                            ext: external,
                        }

                        record external {
                            label: string,
                        }
                    }
                    """)
            #expect(result.diagnostics.isEmpty)  // cross-module ref resolved, not dropped
        }

        @Test(
            .disabled(
                if: ProcessInfo.processInfo.environment["__XCODE_BUILT_PRODUCTS_DIR_PATHS"] != nil,
                "\"swift package resolve\" fails to clone the git repository only under the Xcode test runner"
            )
        )
        func inlinesCrossModuleTypeUsedInFunction() throws {
            let result = try assertSwiftPackage(
                fixturePackage: "CrossModulePackage", ["--target", "CrossModuleFunc"])
            // `generateGuestExportInterface` resolves `external` to `ExternalLib.External`
            // instead of throwing `missingSource`.
            #expect(result.witContents.contains("record external {"))
            #expect(result.witContents.contains("relabel: func(ext: external) -> external;"))
            #expect(result.diagnostics.isEmpty)
        }

        @Test(
            .disabled(
                if: ProcessInfo.processInfo.environment["__XCODE_BUILT_PRODUCTS_DIR_PATHS"] != nil,
                "\"swift package resolve\" fails to clone the git repository only under the Xcode test runner"
            )
        )
        func crossModuleFixtureCompiles() throws {
            try assertSwiftBuilds(fixturePackage: "CrossModulePackage")
        }
    #endif
}
