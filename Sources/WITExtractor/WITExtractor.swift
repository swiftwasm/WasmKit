@available(iOS, unavailable)
@available(watchOS, unavailable)
@available(tvOS, unavailable)
@available(visionOS, unavailable)
package struct WITExtractor {
    package struct Output {
        package var witContents: String
        package var interfaceName: String
        package var sourceSummary: SwiftSourceSummary
    }

    let namespace: String
    let packageName: String
    let sources: [String]
    /// A `@WIT` decl referencing one of these types inlines its definition into the emitted interface.
    let dependencySources: [String: [String]]
    let diagnosticCollection = DiagnosticCollection()

    package var diagnostics: [Diagnostic] {
        diagnosticCollection.diagnostics
    }

    package init(
        namespace: String, packageName: String, sources: [String],
        dependencySources: [String: [String]] = [:]
    ) {
        self.namespace = namespace
        self.packageName = packageName
        self.sources = sources
        self.dependencySources = dependencySources
    }

    package func run(moduleName: String) -> Output {
        let header = """
            // DO NOT EDIT.
            //
            // Generated by the WITExtractor

            """
        var output = runWithoutHeader(moduleName: moduleName)
        output.witContents = header + output.witContents
        return output
    }

    func runWithoutHeader(moduleName: String) -> Output {
        // Collection scans only main trees so a dependency's stray `@WIT` does not leak into the interface.
        // Dependency sources merge first so `build(tagged:)`'s last-insert-wins lets a main-module nominal win
        // a bare-name collision against a dependency's same-named type.
        let mainSources = sources.map { ModuleSource(module: moduleName, source: $0) }
        let depSources = dependencySources.flatMap { module, contents in
            contents.map { ModuleSource(module: module, source: $0) }
        }
        let symbolTable = WITSymbolTableBuilder.build(tagged: depSources + mainSources)

        let collected = WITDeclCollector.collect(parsed: mainSources.map(\.tree), into: diagnosticCollection)
        let widened = InlineClosure(
            resolver: TypeResolver(symbolTable: symbolTable),
            mainModule: moduleName,
            diagnostics: diagnosticCollection
        ).widen(collected)
        // Resolve WIT-name collisions before the emit gate, so a collision-dropped type is absent from
        // `emitted` and references to it drop.
        let inventory = widened.resolvingInterfaceNameCollisions(diagnostics: diagnosticCollection)

        let emitted = Set(inventory.types.map { $0.scopePath + [$0.name] })
        let resolver = TypeResolver(symbolTable: symbolTable, emittedNominals: emitted)

        var translation = ModuleTranslation(
            diagnostics: diagnosticCollection,
            resolver: resolver,
            builder: WITBuilder(interfaceName: ConvertCase.witIdentifier(identifier: [moduleName])))
        translation.translate(inventory: inventory)

        let printer = SourcePrinter(
            header: """
                package \(ConvertCase.witIdentifier(identifier: namespace)):\(ConvertCase.witIdentifier(identifier: packageName));

                """)
        translation.builder.print(printer: printer)
        return Output(
            witContents: printer.contents,
            interfaceName: translation.builder.interfaceName,
            sourceSummary: SwiftSourceSummary(inventory: inventory))
    }
}
